"""Light-weight Python symbolic-execution / concolic engine.

The engine mirrors the core KLEE idea described in Cadar, Dunbar & Engler
(USENIX OSDI 2008), §2.2 and §3.1:

    "KLEE runs programs symbolically rather than normally.  When program
     execution branches followed on a symbolic value, the system
     (conceptually) follows both branches, on each path maintaining a set
     of constraints called the *path condition* which must hold for
     execution of that path.  When a path terminates or hits a bug, a test
     case can be generated by solving the current path condition for
     concrete values."

We keep the implementation deliberately small:

  * One symbolic input variable ``x`` of Z3 sort ``Int``.
  * A depth-first search over the boolean choices at every branch.
  * At every branch we ask Z3 whether ``path_condition AND taken`` is
    satisfiable.  If yes we recurse with the augmented condition; if no
    we prune that sub-tree.
  * When all branches have been resolved (i.e. we have reached the end of
    the function under test) we ask Z3 for a concrete witness, and emit
    that witness as a test case.

The function-under-test itself lives in :mod:`target`.  We *do not* parse
or interpret Python bytecode; we simply hand-write a parallel
``branch_conditions(x, depth)`` table that returns the (taken, not_taken)
Z3 expressions for the same five ``if`` statements the concrete
implementation contains.  This is the same "symbolic interpretation"
approach KLEE performs on LLVM instructions, just at a much higher level.
"""

from __future__ import annotations

import random
from dataclasses import dataclass, field
from typing import Iterable

from z3 import And, Int, Or, sat, Solver

import target

# ----------------------------------------------------------------- branch table
# The order here must match target.ALL_BRANCHES.
def branch_conditions(x, depth: int):
    """Return ``(true_cond, false_cond)`` Z3 expressions for branch *depth*.

    These mirror the boolean test of each ``if`` statement in
    :func:`target.f`, with the simplification that the *taken* side is
    always the boolean in the source code (e.g. ``x == 12345``, not the
    negation).  The negation is added by the caller when the false arm is
    explored.
    """
    if depth == 0:                                 # B0
        return x == 12345
    if depth == 1:                                 # B1
        return x < 0
    if depth == 2:                                 # B2
        return x % 2 == 0
    if depth == 3:                                 # B3
        return x > 1000
    if depth == 4:                                 # B4
        return x % 3 == 0
    raise IndexError(f"no branch at depth {depth}")


# ------------------------------------------------------------- path bookkeeping
@dataclass
class SymbolicPath:
    """A single fully-resolved path through the function."""

    constraints: list                   # list[z3.BoolRef]
    taken_edges: list = field(default_factory=list)  # list[(branch_id, taken)]
    concrete_input: int | None = None
    result_label: str | None = None


# --------------------------------------------------------------------- engine
class SymEx:
    """Symbolic execution engine, parameterised by an input range and a seed.

    The seed is used for tie-breaking when Z3 returns multiple models and
    for the (optional) random state-scheduling that the experiment driver
    may toggle; with depth-first search it has no effect on coverage but
    is preserved for protocol compliance with the task description.
    """

    def __init__(self, x_range: tuple[int, int], seed: int = 0):
        self.x_range = x_range
        self.seed = seed
        self.rng = random.Random(seed)
        # The single symbolic input variable.
        self.x = Int("x")
        # Pre-build range constraints once.
        self._range_pc = [self.x >= x_range[0], self.x <= x_range[1]]

    # ............................................................ public API
    def run(self) -> list[SymbolicPath]:
        """Enumerate every feasible path; return a list of *SymbolicPath*."""
        paths: list[SymbolicPath] = []
        self._dfs(
            current_pc=list(self._range_pc),
            depth=0,
            taken_edges=[],
            out=paths,
        )
        return paths

    # .............................................................. internals
    def _dfs(self, *, current_pc, depth, taken_edges, out):
        if depth == len(target.ALL_BRANCHES):
            # Leaf: we have a fully-resolved path.  Solve for a concrete
            # witness and record it.
            solver = Solver()
            solver.add(*current_pc)
            if solver.check() == sat:
                model = solver.model()
                x_val = model.eval(self.x, model_completion=True).as_long()
                out.append(
                    SymbolicPath(
                        constraints=list(current_pc),
                        taken_edges=list(taken_edges),
                        concrete_input=x_val,
                    )
                )
            return

        branch_id = target.ALL_BRANCHES[depth]
        true_cond = branch_conditions(self.x, depth)
        false_cond = Or(NotWrapper(true_cond))   # see helper below

        # 1) take the *true* arm
        pc_true = current_pc + [true_cond]
        solver_true = Solver()
        solver_true.add(*pc_true)
        if solver_true.check() == sat:
            self._dfs(
                current_pc=pc_true,
                depth=depth + 1,
                taken_edges=taken_edges + [(branch_id, True)],
                out=out,
            )

        # 2) take the *false* arm
        pc_false = current_pc + [false_cond]
        solver_false = Solver()
        solver_false.add(*pc_false)
        if solver_false.check() == sat:
            self._dfs(
                current_pc=pc_false,
                depth=depth + 1,
                taken_edges=taken_edges + [(branch_id, False)],
                out=out,
            )


# Tiny helper -- z3.Not is already available but we wrap it so that the
# call site is symmetric with the true arm.
def NotWrapper(expr):
    from z3 import Not
    return Not(expr)


# --------------------------------------------------------------- convenience
def enumerate_paths(x_range: tuple[int, int], seed: int = 0) -> list[SymbolicPath]:
    """One-shot helper used by the experiment driver."""
    return SymEx(x_range=x_range, seed=seed).run()


def run_and_measure(x_range: tuple[int, int], seed: int = 0):
    """Run the engine and return ``(test_inputs, tracker)`` for the host
    function-under-test.

    The engine itself does *not* execute ``target.f``; it only reasons
    about its control-flow graph.  We then re-execute the host function
    with each generated concrete input to (a) confirm the engine's model
    is correct and (b) populate a :class:`target.BranchTracker` for
    coverage measurement.
    """
    engine = SymEx(x_range=x_range, seed=seed)
    paths = engine.run()
    tracker = target.BranchTracker()
    inputs: list[int] = []
    for p in paths:
        if p.concrete_input is None:
            continue
        target.f(p.concrete_input, tracker)
        inputs.append(p.concrete_input)
    return inputs, tracker, paths
